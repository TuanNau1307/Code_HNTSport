    public void showTable() {
        
        for (ProductDetail r : list) {
            model.addRow(new Object[]{
                r.getId(),r.getImage(),r.getSize(),r.getQuantify(),r.getProduct()
            });
        }
    }
    public void showResult() {
        model.setRowCount(0);
        
        for (ProductDetail r : list) {
            model.addRow(new Object[]{
                r.getId(),r.getImage(),r.getSize(),r.getQuantify(),r.getProduct()
            });
        }
    }


