    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        if (txtProduct.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Hãy nhập sản phẩm cần tìm!");
        } else {
            list = SvDAO.getListSearchProduct(txtProduct.getText());
            showResult();
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        ProductDetailController SvInfomationDAO = new ProductDetailController();
        ProductDetail r = new ProductDetail();
        try {
            if (txtImage.getText().equals("")||txtQuatify.getText().equals("")
                    ||txtSize.getText().equals("")||txtProduct.getText().equals("")
                    ) {
                JOptionPane.showMessageDialog(this, "Hãy điền đầy đủ thông tin!");
            } else {
                r.setImage(txtImage.getText());
                r.setQuantify(Integer.parseInt(txtQuatify.getText()));
                r.setSize(txtSize.getText());
                r.setProduct(Integer.parseInt(txtProduct.getText()));
            
             
                if(SvInfomationDAO.addProductDetail(r)) {
                list.add(r);
                JOptionPane.showMessageDialog(rootPane, "Đã thêm sản phẩm thành công!");
                showResult();
                txtID.setText("");
                txtImage.setText("");
                txtQuatify.setText("");
                txtProduct.setText("");
                
            } else {
                JOptionPane.showMessageDialog(rootPane, "Lỗi ko thêm được !");
            }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelectActionPerformed
        // TODO add your handling code here:
        ProductDetail select = new ProductDetail();
        selectedIndex = tblDetail.getSelectedRow();
        select = list.get(selectedIndex);
        txtID.setText(String.valueOf(select.getId()));
        txtID.setEditable(false);
        txtImage.setText(select.getImage());
        txtQuatify.setText(String.valueOf(select.getQuantify()));
        txtSize.setText(select.getSize());
        txtProduct.setText(String.valueOf(select.getProduct()));
        showResult();
    }//GEN-LAST:event_btnSelectActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
         ProductDetail r = new ProductDetail();
        r.setImage(txtImage.getText());
                r.setQuantify(Integer.parseInt(txtQuatify.getText()));
                r.setSize(txtSize.getText());
                r.setProduct(Integer.parseInt(txtProduct.getText()));
        if (SvDAO.editProductDetail(r, txtID.getText())) {
            SvDAO.editProductDetail(r, txtID.getText());
            JOptionPane.showMessageDialog(rootPane, "Cập nhật sản phẩm thành công!");
            list.clear();
            list = SvDAO.getListProductDetail();
            showResult();
            txtID.setText("");
            txtImage.setText("");
            txtQuatify.setText("");
            txtProduct.setText("");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Cập nhật không thành công!");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

